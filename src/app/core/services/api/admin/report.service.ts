import { Injectable } from '@angular/core';
import {Net, NetMethod, NetService} from "../../../../commons/net/net.service";
import {Endpoint} from "../../../../commons/net/endpoint";
import {map} from "rxjs";

@Injectable({
  providedIn: 'root'
})
export class ReportService {
  constructor(private netService: NetService) { }
  getReport(data:any){
    const net = new Net(NetMethod.post, Endpoint.withUrl(Endpoint.REPORT), data);
    return this.netService.process(net).pipe(
      map((response) => {
        if (response) {
          return response;
        }
        return null;
      })
    );

  }
}
